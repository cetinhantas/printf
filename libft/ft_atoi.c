#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	sign;
	int	total;

	total = 0;
	sign = 1;
	while (*nptr == ' ' || (*nptr >= 9 && *nptr <= 13))
		nptr++;
	if (*nptr == '-' || *nptr == '+')
		if (*nptr++ == '-')
			sign *= -1;
	while (ft_isdigit(*nptr))
	{
		total = total * 10 + (*nptr - 48);
		nptr++;
	}
	return (sign * total);
}
